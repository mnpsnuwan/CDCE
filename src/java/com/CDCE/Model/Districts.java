package com.CDCE.Model;

/**
 *
 * @author nuwan
 */
public class Districts {
    private int districtId;
    private String districtName;
    private String districtNameSi;
    private String districtNameTa;
    private String provinceId;

    public int getDistrictId() {
        return districtId;
    }

    public void setDistrictId(int districtId) {
        this.districtId = districtId;
    }

    public String getDistrictName() {
        return districtName;
    }

    public void setDistrictName(String districtName) {
        this.districtName = districtName;
    }

    public String getDistrictNameSi() {
        return districtNameSi;
    }

    public void setDistrictNameSi(String districtNameSi) {
        this.districtNameSi = districtNameSi;
    }

    public String getDistrictNameTa() {
        return districtNameTa;
    }

    public void setDistrictNameTa(String districtNameTa) {
        this.districtNameTa = districtNameTa;
    }

    public String getProvinceId() {
        return provinceId;
    }

    public void setProvinceId(String provinceId) {
        this.provinceId = provinceId;
    }
}
